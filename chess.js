                var callbackArg,
                    returnValue = property.apply(object, arguments);

                if(bindings[name]) {
                    if(argGen[name]) {
                        callbackArg = argGen[name].apply(object, [returnValue]);
                    }
                    lib.each(bindings[name], function (callback) {
                        callback(callbackArg);
                    });
                }

